    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        String input = jTextField1.getText().trim();

        if (input.isEmpty() || input.equals("🔍 Search Employee")) {
            loadAttendance(jTable1);
            return;
        }

        try {
            int empId = Integer.parseInt(input);

            String sql = "SELECT ATTEND_ID, EMP_ID, FIRST_NAME, LAST_NAME, ATTEND_DATE, CHECK_IN, CHECK_OUT, STATUS FROM ATTENDANCE_LEAVE WHERE EMP_ID = ?";
            PreparedStatement ps = DatabaseConnection.getConnection().prepareStatement(sql);
            ps.setInt(1, empId);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = new DefaultTableModel(
                new String[]{"Attend ID","Emp ID","First Name","Last Name","Date","Check In","Check Out","Status"}, 0
            );

            boolean found = false;
            while (rs.next()) {
                found = true;
                model.addRow(new Object[]{
                    rs.getInt("ATTEND_ID"), rs.getInt("EMP_ID"),
                    rs.getString("FIRST_NAME"), rs.getString("LAST_NAME"),
                    rs.getDate("ATTEND_DATE"), rs.getString("CHECK_IN"),
                    rs.getString("CHECK_OUT"), rs.getString("STATUS")
                });
            }

            jTable1.setModel(model);

            if (!found) {
                javax.swing.JOptionPane.showMessageDialog(this, "No Data Found", "Search Result", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid Employee ID (numbers only)", "Invalid Input", javax.swing.JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

