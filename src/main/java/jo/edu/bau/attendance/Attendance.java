    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select an employee first", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.time.LocalTime now = java.time.LocalTime.now();
        String currentTime = String.format("%02d:%02d", now.getHour(), now.getMinute());

        String time = (String) javax.swing.JOptionPane.showInputDialog(
            this, "Check Out Time:", "Check Out",
            javax.swing.JOptionPane.PLAIN_MESSAGE, null, null, currentTime
        );

        if (time == null) return;

        try {
            int attendId = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE ATTENDANCE_LEAVE SET CHECK_OUT = ? WHERE ATTEND_ID = ?";
            PreparedStatement ps = DatabaseConnection.getConnection().prepareStatement(sql);
            ps.setString(1, time);
            ps.setInt(2, attendId);
            ps.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(this, "Check Out recorded: " + time, "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            loadAttendance(jTable1);
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed
